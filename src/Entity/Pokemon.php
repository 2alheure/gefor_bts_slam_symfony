<?php

namespace App\Entity;

use App\Repository\PokemonRepository;
use Doctrine\DBAL\Types\Types;
use Doctrine\ORM\Mapping as ORM;

#[ORM\Entity(repositoryClass: PokemonRepository::class)]
class Pokemon implements \JsonSerializable {
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    #[ORM\Column(length: 255)]
    private ?string $nom = null;

    #[ORM\Column]
    private ?int $num = null;

    #[ORM\Column(type: Types::TEXT)]
    private ?string $description = null;

    #[ORM\Column(type: Types::TEXT, nullable: true)]
    private ?string $zone = null;

    #[ORM\Column(type: Types::TEXT, nullable: true)]
    private ?string $img = null;

    #[ORM\ManyToOne]
    #[ORM\JoinColumn(nullable: false)]
    private ?Type $type1 = null;

    #[ORM\ManyToOne]
    private ?Type $type2 = null;

    public function jsonSerialize(): mixed {
        return [
            'id' => $this->getId(),
            'nom' => $this->getNom(),
            'num' => $this->getNum(),
            'description' => $this->getDescription(),
            'zone' => $this->getZone(),
            'img' => $this->getImg(),
            'type1' => $this->getType1()->getNom(),
            'type2' => $this->getType2() ? $this->getType2()->getNom() : null,
        ];
    }

    public function getId(): ?int {
        return $this->id;
    }

    public function getNom(): ?string {
        return $this->nom;
    }

    public function setNom(string $nom): static {
        $this->nom = $nom;

        return $this;
    }

    public function getNum(): ?int {
        return $this->num;
    }

    public function setNum(int $num): static {
        $this->num = $num;

        return $this;
    }

    public function getDescription(): ?string {
        return $this->description;
    }

    public function setDescription(string $description): static {
        $this->description = $description;

        return $this;
    }

    public function getZone(): ?string {
        return $this->zone;
    }

    public function setZone(?string $zone): static {
        $this->zone = $zone;

        return $this;
    }

    public function getImg(): ?string {
        return $this->img;
    }

    public function setImg(?string $img): static {
        $this->img = $img;

        return $this;
    }

    public function getType1(): ?Type {
        return $this->type1;
    }

    public function setType1(?Type $type1): static {
        $this->type1 = $type1;

        return $this;
    }

    public function getType2(): ?Type {
        return $this->type2;
    }

    public function setType2(?Type $type2): static {
        $this->type2 = $type2;

        return $this;
    }
}
